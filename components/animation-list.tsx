interface AnimationListProps {
  onSelect: (animationName: string) => void
}

export function AnimationList({ onSelect }: AnimationListProps) {
  const [searchTerm, setSearchTerm] = useState("")

  const filteredAnimations = useMemo(() => {
    if (!searchTerm.trim()) {
      return ANIMATION_LIST
    }
    const search = searchTerm.toLowerCase()
    return ANIMATION_LIST.filter((name) =>
      name.toLowerCase().includes(search)
    )
  }, [searchTerm])

  return (
    <div className="space-y-4">
      <div>
        <h2 className="text-2xl font-bold text-foreground mb-2">Select Animation</h2>
        <p className="text-sm text-muted-foreground mb-4">
          Choose the animation name for your exported file
        </p>
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
          <Input
            type="text"
            placeholder="Search animations..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="pl-10"
          />
        </div>
      </div>

      <div className="space-y-2">
        {filteredAnimations.length === 0 ? (
          <p className="text-sm text-muted-foreground text-center py-8">
            No animations found matching "{searchTerm}"
          </p>
        ) : (
          filteredAnimations.map((animationName) => (
            <Card
              key={animationName}
              className="p-4 cursor-pointer transition-colors hover:bg-primary/10 hover:border-primary"
              onClick={() => onSelect(animationName)}
            >
              <span className="text-sm font-mono font-medium">
                {animationName}
              </span>
            </Card>
          ))
        )}
      </div>
    </div>
  )
}
